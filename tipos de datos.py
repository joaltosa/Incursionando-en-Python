print ('tipos de datos')
# Enteros y largos 'int'
n1 = 15875
print (n1 , type (n1))

# Reales o decimales  'float'
n2 = 3.1415926538
print (n2 , type (n2))

# Complejos  'complex'
n3 = - 3 - 5j
print (n3, type (n3))

# Texto o cadena  'string'
n4 = 'Jorge Alberto'
print (n4, type (n4))

# Booleano  'true or false'
n5 = 3 > 7
print (n5, type (n5))